    <p class="lead-intro">New Zealand licensed immigration advisers are skilled people who have met immigration adviser competency standards and who follow a professional code of conduct. Your New Zealand immigration adviser must either be licensed with the Immigration Advisers Authority (the Authority) or exempt. To check if a person is licensed or to see exempt groups, visit the Authority's website www.iaa.govt.nz.</p>

    <h2 class="section">The Licensed Immigration Advisers Code of Conduct</h2>
    <p>The code of conduct ensures that your licensed immigration adviser will:</p>
    <ul>
        <li>show you evidence that they are licensed</li>
        <li>be honest, professional, diligent and respectful</li>
        <li>conduct themselves with due care and in a timely manner</li>
        <li>provide you with objective advice</li>
        <li>declare any conflicts of interest, including any commission they will receive, and seek your approval to continue to act for you</li>
        <li>provide you with a written agreement for you to sign before any work is started which sets out the services to be provided and the fees</li>
        <li>charge fees that are fair and reasonable</li>
        <li>provide you with invoices with full descriptions of what the fees relate to</li>
        <li>let you know when your application has been lodged and provide you with on-going timely updates</li>
        <li>keep your personal documents safe and return them to you in a secure manner.</li>
    </ul>
    <p>If you experience a problem with your licensed immigration adviser you should try and resolve it with them.</p>

    <h2 class="section">The Immigration Advisers Authority</h2>
    <p>The Immigration Advisers Authority:</p>
    <ul>
        <li>ensures that only suitable people are licensed to provide immigration advice</li>
        <li>keeps a public register of licensed immigration advisers</li>
        <li>develops and maintains competency standards and a code of conduct for licensed advisers</li>
        <li>investigates and takes enforcement action against offences under the Immigration Advisers Licensing Act 2007, such as providing advice when neither licensed nor exempt</li>
        <li>receives complaints about licensed immigration advisers and, if appropriate, refers them to the Immigration Advisers Complaints and Disciplinary Tribunal who will hear the complaint and may impose sanctions.</li>
    </ul>
    <p>The Authority cannot help you with your visa application. Visit www.iaa.govt.nz for more information and:</p>
    <ul><li>The Licensed Immigration Advisers Code of Conduct.</li></ul>

